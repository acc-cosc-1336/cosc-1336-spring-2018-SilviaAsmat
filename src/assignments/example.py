names = ['Silvia', 'Andre','Yrua', 'Paul', 'Marcelo']

"""A for loop is a count-controlled loop"""
for i in range(0, len(names)):
    print(names[i])

"""A while loop is a control flow statement that relies on a boolean expression
A boolean expression is a logical True or False statement"""

i = 0
while i <= 4:
    print(names[i])
    i += 1
